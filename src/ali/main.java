    private void jButton26MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton26MouseClicked
        try {
            int n = Integer.parseInt(this.tamaño_tipo.getText());
            double m[][] = new double[n][n];
            double t[][] = new double[n][n];

            double r[] = new double[n];
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < n; j++) {
                    m[i][j] = Double.parseDouble(String.valueOf(this.table_tipo.getValueAt(i, j)));
                }
            }
            t = this.transpuesta(m, n, n);
            if (identidad(m, n) == true) {
                JOptionPane.showMessageDialog(null, "La matriz es identidad");
            } else if (identidad(m, n) == false) {
                if (triangularsup(m, n) == true) {
                    JOptionPane.showMessageDialog(null, "La matriz es triangular superior");
                } else if (triangularinf(m, n) == true) {
                    JOptionPane.showMessageDialog(null, "La matriz es triangular inferior");
                } else if (this.comparar(m, t) == true) {
                    JOptionPane.showMessageDialog(null, "La matriz es simetrica");
                } else if (this.antisimetrica(m) == true) {
                    JOptionPane.showMessageDialog(null, "La matriz es antisimetrica");
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error asegurese de haber ingresado todos los valores");
        }

    }//GEN-LAST:event_jButton26MouseClicked

