    public static void gaussian(double a[][], int index[]) {
        int n = index.length;
        double c[] = new double[n];

        // Initialize the index
        for (int i = 0; i < n; ++i) {
            index[i] = i;
        }

        // Find the rescaling factors, one from each row
        for (int i = 0; i < n; ++i) {
            double c1 = 0;
            for (int j = 0; j < n; ++j) {
                double c0 = Math.abs(a[i][j]);
                if (c0 > c1) {
                    c1 = c0;
                }
            }
            c[i] = c1;
        }

        // Search the pivoting element from each column
        int k = 0;
        for (int j = 0; j < n - 1; ++j) {
            double pi1 = 0;
            for (int i = j; i < n; ++i) {
                double pi0 = Math.abs(a[index[i]][j]);
                pi0 /= c[index[i]];
                if (pi0 > pi1) {
                    pi1 = pi0;
                    k = i;
                }
            }

            // Interchange rows according to the pivoting order
            int itmp = index[j];
            index[j] = index[k];
            index[k] = itmp;
            for (int i = j + 1; i < n; ++i) {
                double pj = a[index[i]][j] / a[index[j]][j];

                // Record pivoting ratios below the diagonal
                a[index[i]][j] = pj;

                // Modify other elements accordingly
                for (int l = j + 1; l < n; ++l) {
                    a[index[i]][l] -= pj * a[index[j]][l];
                }
            }
        }
    }

    static double determinante(double[][] a) {
        int n = a.length - 1;
        if (n < 0) {
            return 0;
        }
        double M[][][] = new double[n + 1][][];

        M[n] = a;  // init first, largest, M to a

        // create working arrays
        for (int i = 0; i < n; i++) {
            M[i] = new double[i + 1][i + 1];
        }

        return getDecDet(M, n);
    } // end method getDecDet double [][] parameter

    static double getDecDet(double[][][] M, int m) {
        if (m == 0) {
            return M[0][0][0];
        }
        int e = 1;

        // init subarray to upper left mxm submatrix
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < m; j++) {
                M[m - 1][i][j] = M[m][i][j];
            }
        }
        double sum = M[m][m][m] * getDecDet(M, m - 1);

        // walk through rest of rows of M
        for (int i = m - 1; i >= 0; i--) {
            for (int j = 0; j < m; j++) {
                M[m - 1][i][j] = M[m][i + 1][j];
            }
            e = -e;
            sum += e * M[m][i][m] * getDecDet(M, m - 1);
        } // end for each row of matrix

        return sum;
    } // end getDecDet double [][][], int

    public static boolean triangularsup(double m[][], int f) {
        boolean devolver = false;
        for (int i = 0; i < f; i++) {
            for (int j = 0; j < f; j++) {
                if (i > j && m[i][j] == 0) {
                    devolver = true;
                } else if (i > j && m[i][j] != 0) {
                    devolver = false;
                    break;
                }
            }
        }
        return devolver;
    }

