    public static boolean identidad(double m[][], int f) {
        boolean devolver = true;
        for (int i = 0; i < f; i++) {
            for (int j = 0; j < f; j++) {
                if (i == j && m[i][j] == 1) {
                    devolver = true;
                }
                if (i != j && m[i][j] != 0) {
                    devolver = false;
                    break;
                }
            }
            if (devolver == false) {
                break;
            }
        }
        return devolver;
    }

